//7. Faça um algoritmo que imprima na tela os 10 primeiros números inteiros maiores que 100 utilizando um a estrutura de repetição.

//Bibliotecas.
#include<stdio.h>
#include<math.h>

int main (void)
{
    //Declaração de variáveis / Inicío do loop.
    for (int i = 100; i <= 110; i++)
    {
        printf("%i\n", i);
    }
    return 0;
}