//1. Faça um algoritmo para imprimir os números de 1 a 10 utilizando uma estrutura ENQUANTO e um contador.

//Bibliotecas.
#include <stdio.h>
#include <math.h>

int main(void)
{
    //Declaração de variáveis.
    int n = 1;
    //Loop.
    while (n <= 10)
    {
        printf("%i\n", n);
        n++;
    }//fim do loop.
    return 0;
}